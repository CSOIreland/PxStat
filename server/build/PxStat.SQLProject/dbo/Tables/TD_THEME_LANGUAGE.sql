CREATE TABLE [dbo].[TD_THEME_LANGUAGE] (
    [TLG_ID]     INT            IDENTITY (1, 1) NOT NULL,
    [TLG_VALUE]  NVARCHAR (256) NOT NULL,
    [TLG_LNG_ID] INT            NOT NULL,
    [TLG_THM_ID] INT            NOT NULL,
    CONSTRAINT [PK_TD_THEME_LANGUAGE] PRIMARY KEY CLUSTERED ([TLG_ID] ASC),
    CONSTRAINT [FK_TD_THEME_LANGUAGE_TD_THEME] FOREIGN KEY ([TLG_THM_ID]) REFERENCES [dbo].[TD_THEME] ([THM_ID]),
    CONSTRAINT [FK_TD_THEME_LANGUAGE_TS_LANGUAGE] FOREIGN KEY ([TLG_LNG_ID]) REFERENCES [dbo].[TS_LANGUAGE] ([LNG_ID])
);


GO
CREATE NONCLUSTERED INDEX [IX_TLG_LNG_ID]
    ON [dbo].[TD_THEME_LANGUAGE]([TLG_LNG_ID] ASC);


GO
CREATE NONCLUSTERED INDEX [IX_TLG_THM_ID]
    ON [dbo].[TD_THEME_LANGUAGE]([TLG_THM_ID] ASC);

