CREATE TABLE [dbo].[TD_DATA] (
    [TDT_ID]     BIGINT         IDENTITY (1, 1) NOT NULL,
    [TDT_VALUE]  NVARCHAR (MAX) NULL,
    [TDT_STT_ID] INT            NOT NULL,
    [TDT_PRD_ID] INT            NOT NULL,
    [TDT_IX]     INT            NOT NULL,
    [TDT_MTR_ID] INT            NOT NULL,
    CONSTRAINT [PK_TD_DATA] PRIMARY KEY CLUSTERED ([TDT_ID] ASC)
);


GO
CREATE NONCLUSTERED INDEX [IX_TDT_OPTIMISER]
    ON [dbo].[TD_DATA]([TDT_IX] ASC, [TDT_MTR_ID] ASC, [TDT_STT_ID] ASC, [TDT_PRD_ID] ASC)
    INCLUDE([TDT_ID], [TDT_VALUE]);

