    ON [dbo].[TD_GROUP]([GRP_DELETE_FLAG] ASC);


GO
CREATE NONCLUSTERED INDEX [IX_GRP_DTG_ID]
    ON [dbo].[TD_GROUP]([GRP_DTG_ID] ASC);


GO
CREATE NONCLUSTERED INDEX [IX_GRP_NAME]
    ON [dbo].[TD_GROUP]([GRP_NAME] ASC);

