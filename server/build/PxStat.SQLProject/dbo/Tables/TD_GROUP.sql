CREATE TABLE [dbo].[TD_GROUP] (
    [GRP_ID]            INT            IDENTITY (1, 1) NOT NULL,
    [GRP_CODE]          NVARCHAR (32)  NOT NULL,
    [GRP_NAME]          NVARCHAR (256) NOT NULL,
    [GRP_CONTACT_NAME]  NVARCHAR (256) CONSTRAINT [DF_TD_GROUP_GRP_CONTACT_NAME] DEFAULT (NULL) NULL,
    [GRP_CONTACT_PHONE] NVARCHAR (256) CONSTRAINT [DF_TD_GROUP_GRP_CONTACT_PHONE] DEFAULT (NULL) NULL,
    [GRP_CONTACT_EMAIL] NVARCHAR (256) NOT NULL,
    [GRP_DTG_ID]        INT            NOT NULL,
    [GRP_DELETE_FLAG]   BIT            CONSTRAINT [DF_TD_GROUP_GRP_DELETE_FLAG] DEFAULT ((0)) NOT NULL,
    CONSTRAINT [PK_TD_GROUP] PRIMARY KEY CLUSTERED ([GRP_ID] ASC),
    CONSTRAINT [FK_TD_GROUP_TD_AUDITING] FOREIGN KEY ([GRP_DTG_ID]) REFERENCES [dbo].[TD_AUDITING] ([DTG_ID])
);


GO
CREATE NONCLUSTERED INDEX [IX_GRP_CODE]
    ON [dbo].[TD_GROUP]([GRP_CODE] ASC);


GO
