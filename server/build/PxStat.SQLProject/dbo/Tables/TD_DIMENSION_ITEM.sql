CREATE TABLE [dbo].[TD_DIMENSION_ITEM] (
    [DMT_ID]               INT            IDENTITY (1, 1) NOT NULL,
    [DMT_CODE]             NVARCHAR (256) NOT NULL,
    [DMT_VALUE]            NVARCHAR (256) NOT NULL,
    [DMT_SEQUENCE]         INT            NOT NULL,
    [DMT_MDM_ID]           INT            NOT NULL,
    [DMT_ELIMINATION_FLAG] BIT            CONSTRAINT [DF_TD_DIMENSION_ITEM_DMT_ELIMINATION_FLAG] DEFAULT ((0)) NOT NULL,
    [DMT_DECIMAL]          TINYINT        CONSTRAINT [DF_TD_DIMENSION_ITEM_DMT_DECIMAL] DEFAULT ((0)) NOT NULL,
    [DMT_UNIT]             NVARCHAR (256) NULL,
    CONSTRAINT [PK_TD_DIMENSION_ITEM] PRIMARY KEY CLUSTERED ([DMT_ID] ASC),
    CONSTRAINT [FK_TD_DIMENSION_ITEM_TD_MATRIX_DIMENSION] FOREIGN KEY ([DMT_MDM_ID]) REFERENCES [dbo].[TD_MATRIX_DIMENSION] ([MDM_ID])
);




GO
CREATE NONCLUSTERED INDEX [IX_DMT_SEQUENCE_MDM_ID]
    ON [dbo].[TD_DIMENSION_ITEM]([DMT_MDM_ID] ASC, [DMT_SEQUENCE] ASC);


GO
CREATE NONCLUSTERED INDEX [IX_DMT_MDM_ID]
    ON [dbo].[TD_DIMENSION_ITEM]([DMT_MDM_ID] ASC);

