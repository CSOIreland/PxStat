CREATE TABLE [dbo].[TD_STATISTIC] (
    [STT_ID]      INT            IDENTITY (1, 1) NOT NULL,
    [STT_CODE]    NVARCHAR (256) NOT NULL,
    [STT_VALUE]   NVARCHAR (256) NOT NULL,
    [STT_UNIT]    NVARCHAR (256) NOT NULL,
    [STT_DECIMAL] TINYINT        CONSTRAINT [DF_TD_STATISTIC_STT_DECIMAL] DEFAULT ((0)) NOT NULL,
    [STT_MTR_ID]  INT            NOT NULL,
    CONSTRAINT [PK_TD_STATISTIC] PRIMARY KEY CLUSTERED ([STT_ID] ASC),
    CONSTRAINT [FK_TD_STATISTIC_TD_MATRIX] FOREIGN KEY ([STT_MTR_ID]) REFERENCES [dbo].[TD_MATRIX] ([MTR_ID])
);


GO
CREATE NONCLUSTERED INDEX [IX_STT_CODE]
    ON [dbo].[TD_STATISTIC]([STT_CODE] ASC);


GO
CREATE NONCLUSTERED INDEX [IX_STT_MTR_ID]
    ON [dbo].[TD_STATISTIC]([STT_MTR_ID] ASC);

