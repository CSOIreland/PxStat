CREATE TABLE [dbo].[TS_SIGNOFF] (
    [SGN_ID]    INT           IDENTITY (1, 1) NOT NULL,
    [SGN_CODE]  VARCHAR (32)  NOT NULL,
    [SGN_VALUE] VARCHAR (256) NOT NULL,
    CONSTRAINT [PK_TS_SIGNOFF] PRIMARY KEY CLUSTERED ([SGN_ID] ASC)
);


GO
CREATE UNIQUE NONCLUSTERED INDEX [IX_SGN_CODE]
    ON [dbo].[TS_SIGNOFF]([SGN_CODE] ASC);

