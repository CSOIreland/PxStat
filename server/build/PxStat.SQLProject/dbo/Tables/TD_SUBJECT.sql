CREATE TABLE [dbo].[TD_SUBJECT] (
    [SBJ_ID]          INT            IDENTITY (1, 1) NOT NULL,
    [SBJ_CODE]        INT            CONSTRAINT [SBJ_CODE_SEQUENCE] DEFAULT (NEXT VALUE FOR [dbo].[SBJ_CODE_seq]) NOT NULL,
    [SBJ_VALUE]       NVARCHAR (256) NOT NULL,
    [SBJ_LNG_ID]      INT            NOT NULL,
    [SBJ_DTG_ID]      INT            NOT NULL,
    [SBJ_DELETE_FLAG] BIT            CONSTRAINT [DF_TD_SUBJECT_SBJ_DELETE_FLAG] DEFAULT ((0)) NOT NULL,
    [SBJ_THM_ID]      INT            NOT NULL,
    CONSTRAINT [PK_TS_SUBJECT] PRIMARY KEY CLUSTERED ([SBJ_ID] ASC),
    CONSTRAINT [FK_TD_SUBJECT_TD_THEME] FOREIGN KEY ([SBJ_THM_ID]) REFERENCES [dbo].[TD_THEME] ([THM_ID]),
    CONSTRAINT [FK_TD_SUBJECT_TS_LANGUAGE] FOREIGN KEY ([SBJ_LNG_ID]) REFERENCES [dbo].[TS_LANGUAGE] ([LNG_ID]),
    CONSTRAINT [FK_TS_SUBJECT_TD_AUDITING] FOREIGN KEY ([SBJ_DTG_ID]) REFERENCES [dbo].[TD_AUDITING] ([DTG_ID])
);


GO
