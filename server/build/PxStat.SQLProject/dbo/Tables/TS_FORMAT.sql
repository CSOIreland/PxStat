CREATE TABLE [dbo].[TS_FORMAT] (
    [FRM_ID]        INT           IDENTITY (1, 1) NOT NULL,
    [FRM_TYPE]      NVARCHAR (32) NOT NULL,
    [FRM_VERSION]   NVARCHAR (32) NOT NULL,
    [FRM_DIRECTION] VARCHAR (20)  NOT NULL,
    CONSTRAINT [PK_TS_FORMAT_TYPE] PRIMARY KEY CLUSTERED ([FRM_ID] ASC),
    CONSTRAINT [CK_FRM_DIRECTION] CHECK ([FRM_DIRECTION]='UPLOAD' OR [FRM_DIRECTION]='DOWNLOAD')
);


GO
CREATE NONCLUSTERED INDEX [IX_FRM_DIRECTION]
    ON [dbo].[TS_FORMAT]([FRM_DIRECTION] ASC);


GO
CREATE NONCLUSTERED INDEX [IX_FRM_TYPE]
    ON [dbo].[TS_FORMAT]([FRM_TYPE] ASC);


GO
CREATE NONCLUSTERED INDEX [IX_FRM_VERSION]
    ON [dbo].[TS_FORMAT]([FRM_VERSION] ASC);

