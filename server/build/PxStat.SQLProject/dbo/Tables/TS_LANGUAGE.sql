CREATE TABLE [dbo].[TS_LANGUAGE] (
    [LNG_ID]          INT           IDENTITY (1, 1) NOT NULL,
    [LNG_ISO_CODE]    CHAR (2)      NOT NULL,
    [LNG_ISO_NAME]    NVARCHAR (32) NOT NULL,
    [LNG_DTG_ID]      INT           NOT NULL,
    [LNG_DELETE_FLAG] BIT           CONSTRAINT [DF_TS_LANGUAGE_LNG_DELETE_FLAG] DEFAULT ((0)) NOT NULL,
    CONSTRAINT [PK_TS_LANGUAGE] PRIMARY KEY CLUSTERED ([LNG_ID] ASC),
    CONSTRAINT [FK_TS_LANGUAGE_TD_AUDITING] FOREIGN KEY ([LNG_DTG_ID]) REFERENCES [dbo].[TD_AUDITING] ([DTG_ID])
);


GO
CREATE NONCLUSTERED INDEX [IX_LNG_DELETE_FLAG]
    ON [dbo].[TS_LANGUAGE]([LNG_DELETE_FLAG] ASC);


GO
CREATE NONCLUSTERED INDEX [IX_LNG_DTG_ID]
    ON [dbo].[TS_LANGUAGE]([LNG_DTG_ID] ASC);


GO
CREATE NONCLUSTERED INDEX [IX_LNG_ISO_CODE]
    ON [dbo].[TS_LANGUAGE]([LNG_ISO_CODE] ASC);

