CREATE TABLE [dbo].[TD_TRACE] (
    [TRC_ID]        INT             IDENTITY (1, 1) NOT NULL,
    [TRC_METHOD]    VARCHAR (256)   NOT NULL,
    [TRC_PARAMS]    NVARCHAR (2048) NULL,
    [TRC_IP]        VARCHAR (15)    NOT NULL,
    [TRC_USERAGENT] VARCHAR (2048)  NULL,
    [TRC_USERNAME]  NVARCHAR (256)  NULL,
    [TRC_DATETIME]  DATETIME        NOT NULL,
    CONSTRAINT [PK_TD_TRACE] PRIMARY KEY CLUSTERED ([TRC_ID] ASC)
);


GO
CREATE NONCLUSTERED INDEX [IX_TRC_IP]
    ON [dbo].[TD_TRACE]([TRC_IP] ASC);


GO
CREATE NONCLUSTERED INDEX [IX_TRC_USERNAME]
    ON [dbo].[TD_TRACE]([TRC_USERNAME] ASC);


GO
CREATE NONCLUSTERED INDEX [IX_TRC_METHOD]
    ON [dbo].[TD_TRACE]([TRC_METHOD] ASC);


GO
CREATE NONCLUSTERED INDEX [IX_TRC_DATETIME]
    ON [dbo].[TD_TRACE]([TRC_DATETIME] ASC);

