CREATE TABLE [dbo].[TD_THEME] (
    [THM_ID]          INT            IDENTITY (1, 1) NOT NULL,
    [THM_CODE]        INT            CONSTRAINT [THM_CODE_SEQUENCE] DEFAULT (NEXT VALUE FOR [dbo].[THM_CODE_seq]) NOT NULL,
    [THM_VALUE]       NVARCHAR (256) NOT NULL,
    [THM_LNG_ID]      INT            NOT NULL,
    [THM_DTG_ID]      INT            NOT NULL,
    [THM_DELETE_FLAG] BIT            CONSTRAINT [DF_TD_THEME_THM_DELETE_FLAG] DEFAULT ((0)) NOT NULL,
    CONSTRAINT [PK_TD_THEME] PRIMARY KEY CLUSTERED ([THM_ID] ASC),
    CONSTRAINT [FK_TD_THEME_TD_AUDITING] FOREIGN KEY ([THM_DTG_ID]) REFERENCES [dbo].[TD_AUDITING] ([DTG_ID]),
    CONSTRAINT [FK_TD_THEME_TS_LANGUAGE] FOREIGN KEY ([THM_LNG_ID]) REFERENCES [dbo].[TS_LANGUAGE] ([LNG_ID])
);


GO
CREATE NONCLUSTERED INDEX [IX_THM_CODE]
    ON [dbo].[TD_THEME]([THM_CODE] ASC);


GO
CREATE NONCLUSTERED INDEX [IX_THM_DELETE_FLAG]
    ON [dbo].[TD_THEME]([THM_DELETE_FLAG] ASC);


GO
CREATE NONCLUSTERED INDEX [IX_THM_DTG_ID]
    ON [dbo].[TD_THEME]([THM_DTG_ID] ASC);


GO
CREATE NONCLUSTERED INDEX [IX_THM_LNG_ID]
    ON [dbo].[TD_THEME]([THM_LNG_ID] ASC);

