CREATE TABLE [dbo].[TD_MATRIX_DATA] (
    [MTD_ID]      INT            IDENTITY (1, 1) NOT NULL,
    [MTD_MTR_ID]  INT            NOT NULL,
    [MTD_DATA]    NVARCHAR (MAX) COLLATE Latin1_General_BIN2  ENCRYPTED WITH (
     COLUMN_ENCRYPTION_KEY = [CEK_MTD_DATA],
     ALGORITHM = N'AEAD_AES_256_CBC_HMAC_SHA_256',
     ENCRYPTION_TYPE = DETERMINISTIC
    ) NULL,
    [MTD_DECIMAL] TINYINT        CONSTRAINT [DF_TD_MATRIX_DATA_MTD_DECIMAL] DEFAULT ((0)) NOT NULL,
    CONSTRAINT [PK_TD_MATRIX_DATA] PRIMARY KEY CLUSTERED ([MTD_ID] ASC),
    CONSTRAINT [FK_TD_MATRIX_DATA_TD_MATRIX] FOREIGN KEY ([MTD_MTR_ID]) REFERENCES [dbo].[TD_MATRIX] ([MTR_ID])
);




GO
CREATE NONCLUSTERED INDEX [IX_MTD_MTR_ID]
    ON [dbo].[TD_MATRIX_DATA]([MTD_MTR_ID] ASC);

