CREATE TABLE [dbo].[TS_RESPONSE] (
    [RSP_ID]    INT           IDENTITY (1, 1) NOT NULL,
    [RSP_CODE]  VARCHAR (32)  NOT NULL,
    [RSP_VALUE] VARCHAR (256) NOT NULL,
    CONSTRAINT [PK_TS_RESPONSE] PRIMARY KEY CLUSTERED ([RSP_ID] ASC)
);


GO
CREATE UNIQUE NONCLUSTERED INDEX [IX_RSP_CODE]
    ON [dbo].[TS_RESPONSE]([RSP_CODE] ASC);

