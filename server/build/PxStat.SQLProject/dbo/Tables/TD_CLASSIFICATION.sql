CREATE TABLE [dbo].[TD_CLASSIFICATION] (
    [CLS_ID]       INT             IDENTITY (1, 1) NOT NULL,
    [CLS_CODE]     NVARCHAR (256)  NOT NULL,
    [CLS_VALUE]    NVARCHAR (256)  NOT NULL,
    [CLS_GEO_FLAG] BIT             CONSTRAINT [DF_TD_CLASSIFICATION_CLS_GEO_FLAG] DEFAULT ((0)) NOT NULL,
    [CLS_GEO_URL]  NVARCHAR (2048) NULL,
    [CLS_MTR_ID]   INT             NOT NULL,
    CONSTRAINT [PK_TD_CLASSIFICATION] PRIMARY KEY CLUSTERED ([CLS_ID] ASC),
    CONSTRAINT [FK_TD_CLASSIFICATION_TD_MATRIX] FOREIGN KEY ([CLS_MTR_ID]) REFERENCES [dbo].[TD_MATRIX] ([MTR_ID])
);


GO
