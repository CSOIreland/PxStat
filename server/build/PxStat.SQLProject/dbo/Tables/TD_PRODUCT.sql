CREATE TABLE [dbo].[TD_PRODUCT] (
    [PRC_ID]          INT            IDENTITY (1, 1) NOT NULL,
    [PRC_CODE]        NVARCHAR (32)  NOT NULL,
    [PRC_VALUE]       NVARCHAR (256) NOT NULL,
    [PRC_SBJ_ID]      INT            NOT NULL,
    [PRC_LNG_ID]      INT            NOT NULL,
    [PRC_DTG_ID]      INT            NOT NULL,
    [PRC_DELETE_FLAG] BIT            CONSTRAINT [DF_TD_PRODUCT_PRC_DELETE_FLAG] DEFAULT ((0)) NOT NULL,
    CONSTRAINT [PK_TD_PRODUCT] PRIMARY KEY CLUSTERED ([PRC_ID] ASC),
    CONSTRAINT [FK_TD_PRODUCT_TD_AUDITING] FOREIGN KEY ([PRC_DTG_ID]) REFERENCES [dbo].[TD_AUDITING] ([DTG_ID]),
    CONSTRAINT [FK_TD_PRODUCT_TS_LANGUAGE] FOREIGN KEY ([PRC_LNG_ID]) REFERENCES [dbo].[TS_LANGUAGE] ([LNG_ID]),
    CONSTRAINT [FK_TD_PRODUCT_TS_SUBJECT] FOREIGN KEY ([PRC_SBJ_ID]) REFERENCES [dbo].[TD_SUBJECT] ([SBJ_ID])
);


GO
