CREATE TABLE [dbo].[TD_SUBSCRIBER] (
    [SBR_ID]          INT            IDENTITY (1, 1) NOT NULL,
    [SBR_USR_ID]      INT            NOT NULL,
    [SBR_UID]         NVARCHAR (256) NOT NULL,
    [SBR_PREFERENCE]  NVARCHAR (MAX) NULL,
    [SBR_DELETE_FLAG] BIT            NOT NULL,
    [SBR_KEY]         NVARCHAR (256) NOT NULL,
    CONSTRAINT [PK_TD_SUBSCRIBER] PRIMARY KEY CLUSTERED ([SBR_ID] ASC),
    CONSTRAINT [FK_TD_SUBSCRIBER_TD_USER] FOREIGN KEY ([SBR_USR_ID]) REFERENCES [dbo].[TD_USER] ([USR_ID])
);




GO
CREATE NONCLUSTERED INDEX [IX_SBR_DELETE_FLAG]
    ON [dbo].[TD_SUBSCRIBER]([SBR_DELETE_FLAG] ASC);


GO
CREATE NONCLUSTERED INDEX [IX_SBR_KEY]
    ON [dbo].[TD_SUBSCRIBER]([SBR_KEY] ASC);


GO
CREATE UNIQUE NONCLUSTERED INDEX [IX_SBR_UID]
    ON [dbo].[TD_SUBSCRIBER]([SBR_UID] ASC) WHERE ([SBR_DELETE_FLAG]=(0));




GO
CREATE NONCLUSTERED INDEX [IX_SBR_USR_ID]
    ON [dbo].[TD_SUBSCRIBER]([SBR_USR_ID] ASC);

