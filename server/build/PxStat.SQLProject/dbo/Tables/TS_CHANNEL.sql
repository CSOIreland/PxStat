CREATE TABLE [dbo].[TS_CHANNEL] (
    [CHN_ID]   INT            IDENTITY (1, 1) NOT NULL,
    [CHN_NAME] NVARCHAR (256) NOT NULL,
    [CHN_CODE] NVARCHAR (256) NOT NULL,
    CONSTRAINT [PK_TS_CHANNEL] PRIMARY KEY CLUSTERED ([CHN_ID] ASC)
