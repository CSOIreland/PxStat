CREATE TABLE [dbo].[TS_REQUEST] (
    [RQS_ID]    INT           IDENTITY (1, 1) NOT NULL,
    [RQS_CODE]  VARCHAR (32)  NOT NULL,
    [RQS_VALUE] VARCHAR (256) NOT NULL,
    CONSTRAINT [PK_TS_REQUEST] PRIMARY KEY CLUSTERED ([RQS_ID] ASC)
);


GO
CREATE UNIQUE NONCLUSTERED INDEX [IX_RQS_CODE]
    ON [dbo].[TS_REQUEST]([RQS_CODE] ASC);

